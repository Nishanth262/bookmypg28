// Helper functions
export const getStateById = (stateId: string): State | undefined => {
  return INDIAN_STATES.find(state => state.id === stateId)
}

export const getDistrictsByStateId = (stateId: string): District[] => {
  const state = getStateById(stateId)
  return state ? state.districts : []
}

export const getDistrictById = (districtId: string): District | undefined => {
  for (const state of INDIAN_STATES) {
    const district = state.districts.find(d => d.id === districtId)
    if (district) return district
  }
  return undefined
}

export const getAllDistricts = (): District[] => {
  return INDIAN_STATES.flatMap(state => state.districts)
}